package com.guruofjava.deloitte2.api;

public class MathDemo {

	public static void main(String[] args) {
		System.out.println(Math.PI);
		System.out.println(Math.E);
		
		System.out.println(Math.max(345, 278));
		System.out.println(Math.min(345, 278));
		System.out.println(Math.floor(36.99));
		System.out.println(Math.ceil(36.99));
		System.out.println(Math.round(36.99));


	}

}
